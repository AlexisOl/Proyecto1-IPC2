package model;

/**
 *
 * @author alexis
 */
public class Pieza {

    private int id;
    private String tipo;
    private double costo;
    private int stock;

    public Pieza() {
    }

    public Pieza(int id, String tipo, double costo, int stock) {
        this.id = id;
        this.tipo = tipo;
        this.costo = costo;
        this.stock = stock;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public double getCosto() {
        return costo;
    }

    public void setCosto(double costo) {
        this.costo = costo;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    @Override
    public String toString() {
        return tipo;
    }

}
